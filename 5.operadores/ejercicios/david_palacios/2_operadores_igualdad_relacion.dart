main() {
  final a = 10;
  final b = 5;

  //igualdad ==
  final igualdad = a == b;
  print("La operacion es: $igualdad");

  //Desigualdad !=
  final noIgual = a != b;
  print("La operacion es: $noIgual");

  //mayor que >
  final esMayor = a > b;
  print("La operacion es: $esMayor");

  //menor que <
  final esMenor = a < b;
  print("La operacion es: $esMenor");

  //MAYO O IGUAL QUE >=
  final esMayorIgual = a >= b;
  print("La operacion es: $esMayorIgual");

  //Menor o igual que <=
  final esMenorIgua = a <= b;
  print("La operacion es: $esMenorIgua");
}
